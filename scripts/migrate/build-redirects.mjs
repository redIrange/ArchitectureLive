// scripts/migrate/build-redirects.mjs
// WordPress permalink structure was /%year%/%monthnum%/%postname%/ for posts.
// Project pages lived at /projects/<slug>/ — identical to the new Astro route, no redirect needed.
import { writeFileSync } from "node:fs";
import { extractOne, ARCHIVE } from "./lib/wpress.mjs";
import { rowObjects } from "./lib/sql.mjs";
const sql = extractOne(ARCHIVE, "database.sql").toString("utf8");
const posts = rowObjects(sql, "SERVMASK_PREFIX_posts");
const lines = ["# Generated by build-redirects.mjs — old WordPress URLs → new Astro routes"];
for (const p of posts.filter((x) => x.post_type === "post" && x.post_status === "publish")) {
  const date = (p.post_date || "").slice(0, 10); // "YYYY-MM-DD"
  const yyyy = date.slice(0, 4);
  const mm = date.slice(5, 7);
  const slug = p.post_name;
  if (yyyy && mm) {
    // Real dated WordPress permalink (most inbound links point here)
    lines.push(`/${yyyy}/${mm}/${slug}  /news/${slug}  301`);
  }
  // Root catch-all (handles any bare /<slug> links in the wild)
  lines.push(`/${slug}  /news/${slug}  301`);
}
writeFileSync("public/_redirects", lines.join("\n") + "\n");
const newsCount = posts.filter((x) => x.post_type === "post" && x.post_status === "publish").length;
console.log(`Wrote ${lines.length - 1} redirects (${newsCount} news posts → ${newsCount} dated + ${newsCount} root lines).`);
